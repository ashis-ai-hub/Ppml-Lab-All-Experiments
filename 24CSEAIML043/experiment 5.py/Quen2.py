my_list = [10, 20, 30, 40]
print("List elements:")
for i in my_list:
    print(i)

my_tuple = (1, 2, 3, 4)
print("\nTuple elements:")
for i in my_tuple:
    print(i)

my_dict = {1: "Apple", 2: "Banana", 3: "Mango"}
print("\nDictionary keys and values:")
for key, value in my_dict.items():
    print(key, ":", value)

my_set = {100, 200, 300, 400}
print("\nSet elements:")
for i in my_set:
    print(i)
